class DataTypes {
    public static void main(String[] args) {
        int num = 9;               //int = 4 byte
        byte  by = 127;            //byte = 1 byte
        short sh = 558;            //short = 2 byte
        long l =5854l;             // long = 8 byte

        float f = 5.4f;            // float = 4 byte
        double d = 4.2;            // double = 8 byte

        char c = 'a';              // character = 2 byte

        boolean b = true;          // boolean = JVM dependent

        System.out.println(num);
    }    
}
